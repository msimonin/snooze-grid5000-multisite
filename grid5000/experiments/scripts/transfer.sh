# Synchronized data using rsync
synchronize_with_rsync () {
    echo "$log_tag Synchronizing data $2 to directory $3 on node $1"
    rsync -avz --delete -e "ssh -i $ssh_private_key" $2 root@$1:$3 > /dev/null 2>&1
}

